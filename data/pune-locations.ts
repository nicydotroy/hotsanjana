export function getPuneLocationBySlug(slug: string): PuneLocation | undefined {
  return puneLocations.find(l => l.slug === slug)
}

export function getAllPuneLocationSlugs(): string[] {
  return puneLocations.map(l => l.slug)
}

export const puneLocationsByArea: Record<string, PuneLocation[]> = puneLocations.reduce(
  (acc, location) => {
    const key = location.area
    if (!acc[key]) acc[key] = []
    acc[key].push(location)
    return acc
  },
  {} as Record<string, PuneLocation[]>
)
